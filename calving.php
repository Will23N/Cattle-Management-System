 	<h2>Calving</h2>
 </div>
 <h3>Introduction</h3>
 <p style="font-size:medium;">Calving is a natural process which normally takes place without help. Close observation is required in case the cow has difficulties. Cows calving for the first time (heifers) tend to have more problems than older cows and therefore need more attention when calving.</p>
 <h3>Signs Of Calving</h3>
 <p style="font-size:medium;">You will know that the cow is about to calve or give birth when you see:</p>     
 <ul style="font-size:medium;">
<li style="color: #565656;">The belly has increased in size, especially on the right flank. </li>
<li style="color: #565656;">The udder is filling up and the teats are stiffening. </li>
<li style="color: #565656;">The vulva becomes red and swollen with the presence of mucous and blood colored fluid. </li>
<li style="color: #565656;">The animal is restless. </li>
<li style="color: #565656;">The water bag appears at the vulva. </li>
</ul>
<h3>Normal Calving</h3>
<ul style="font-size:medium;">
<li style="color: #565656;">The water bag appears through the vulva. </li>
<li style="color: #565656;">The cow will strain more. </li>
<li style="color: #565656;">The head of the calf will appear and this breaks the bag. </li>
<li style="color: #565656;">You will then be able to see both of the calf's front feet. </li>
<li style="color: #565656;">It takes 4 - 6 hours for the calving to reach this stage. </li>
<li style="color: #565656;">In heifers, it might take longer. </li>
<li style="color: #565656;">As the chest comes through the vagina the calf starts to breathe. </li>
<li style="color: #565656;">It is better to leave the cow alone to give birth naturally. </li>
<li style="color: #565656;">However, if you want to help with the calving you can gently pull the calf by its feet. </li>
<li style="color: #565656;">If the navel cord is still attached to the cow you can cut it with a clean sharp knife or a pair of scissors, then put tincture of iodine or alcohol on the end of the navel cord. </li>
<li style="color: #565656;">Sometimes the back feet of the calf appear first. You will see that the back feet come out from the vulva with the soles of the feet showing uppermost. You should then look (or feel with your hands) for the tail and the hock joints. </li>
</ul>
<h3>Difficulties in calving</h3>
<p style="font-size:medium;">Leave the animal to give birth naturally. If difficulties occur, you may find:</p>
<ul style="font-size:medium;">
<li style="color: #565656;">Only the head of the calf has appeared. </li>
<li style="color: #565656;">The head and one foot has come out. </li>
<li style="color: #565656;">Two front feet showing but no head. </li>
</ul>
<p style="font-size:medium;">If this happens you should either ask the veterinarian to help.</p>
<p style="font-size:medium;"><strong>Helping a cow having difficulties in Calving</strong></p>
<ul style="font-size:medium;">
<li style="color: #565656;">You will need a bar of soap, hot water, a clean rope and clean vegetable oil such as olive or sunflower oil.</li>
<li style="color: #565656;">Wash the area around the vulva and wash your hands well. </li>
<li style="color: #565656;">Make sure that your fingernails are cut short and are thoroughly clean. Long nails can injure the animal. </li>
<li style="color: #565656;">If you have oil put some over your hand and arm, if not, soap your hand and insert it into the vagina to discover what is wrong. </li>
<li style="color: #565656;">You will need to recognize the difference between the front and back legs of the calf in the womb. </li>
<li style="color: #565656;">Touch the fetlock joint and then run your hand up the leg to the next joint. There will be a knee joint on the front leg and a hock on the back leg. </li>
<li style="color: #565656;">Push the calf either to one side or back into the uterus so that you can correct the situation and move the head and legs into the right place for birth.</li>
<li style="color: #565656;">When the calf's head and legs are in the correct position tie a clean rope around both feet. </li>
<li style="color: #565656;">Pull gently on the rope. You may need someone to help you pull. </li>
<li style="color: #565656;">Sometimes the water bag will burst but neither the feet nor the head will have appeared. This is a very difficult position to sort out and if you can you should immediately ask your veterinarian for help. </li>
</ul>
<h3>Caring for the Cow after Calving</h3>
<ul style="font-size:medium;">
<li style="color: #565656;">Give the cow clean water to drink immediately after she has calved as she will be thirsty. </li>
<li style="color: #565656;">The water bag (afterbirth) will come out naturally but you can help to remove it by gently pulling it. </li>
<li style="color: #565656;">The afterbirth should have come away by 24 hours after the birth. </li>
<li style="color: #565656;">If the afterbirth remains in the uterus it will cause an infection and you will need to get your veterinarian to help. </li>
</ul>
<h3>Caring for the Newborn Calf</h3>
<ul style="font-size:medium;">
<li style="color: #565656;">Always handle the calf carefully. Clean the mucous (sticky fluid) from the nose and mouth and check that the calf is breathing normally. If it is not breathing you must act immediately by:</li>
<ul style="font-size:medium;">
<li style="color: #565656;">Pump the chest with the palm of your hand. </li>
<li style="color: #565656;">Keep the calf's head lower than its back. </li>
<li style="color: #565656;">Insert a straw into its nose in an attempt to make it sneeze and start breathing </li>
</ul>
<li style="color: #565656;">Allow the calf to suckle from its mother as soon as possible so that it takes in the colostrum, the yellowish milk which is produced immediately after birth. The colostrum is rich in protein and protects the calf against disease. </li>
<li style="color: #565656;">Some people use the colostrum for their food but it is essential to make the calf strong and healthy and should be left for the calf. </li>
<li style="color: #565656;">You must allow the calf to take colostrum for at least four days after its birth.</li>
</ul>