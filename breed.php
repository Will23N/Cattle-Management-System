<ol style="font-size:medium;">
<li style="color: #565656;">Due to deficient luteinizing hormone release, delayed ovulation or failure of ovulation may leads to fertilization failure.</li>
<li style="color: #565656;">Defective ovum or ageing of ovum may leads to fertilization failure.</li>
<li style="color: #565656;">Inability of the sperm to fertilize a viable ovum.</li>
<li style="color: #565656;">Inability of gametes to reach one another.</li>
<li style="color: #565656;">The organisms <i>Trichomonas fetus, Campylobacter fetus, Brucella abortus</i> and IBR-IPV which may cause early embryonic death.</li>
<li style="color: #565656;">Deficiency of Selenium and Vitamin E may cause early embryonic death.</li>
<li style="color: #565656;">Long period of feeding estrogenic forages may affect the embryo survival.</li>
<li style="color: #565656;">Environmental stress during first week after breeding may lead to early embryonic death.</li>
