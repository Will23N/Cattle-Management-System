<p style="font-size:medium;"><strong>Management</strong></p>
<ol style="font-size:medium;">
<li style="color: #565656;">Bring the animal into positive nutritive balance.</li>
<li style="color: #565656;">Mineral mixture supplementation should be done to breeding animals.</li>
<li style="color: #565656;">Do Artificial Insemination twice at each oestrus preferably at 12 or 24 hrs intervals.</li>
<li style="color: #565656;">Skipping of AI and intrauterine infusions may be considered for uterine pathology.</li>
<li style="color: #565656;">Diseased bulls should not be allowed for breeding.</li>
<li style="color: #565656;">By avoiding diseased breeding bulls the pathogenic organisms causing abortion may be controlled.</li>
</ol>
<h3>Infertility in Cattle – Causes and Treatment</h3>
<p style="font-size:medium;">In cattle, nearly 10-30 per cent of lactations may be affected by infertility and reproductive disorders. To attain good fertility or high calving rate, both the male and female animals should be well fed and free from diseases.</p>
<strong style="font-size:medium;">Reasons for infertility</strong>
<p style="font-size:medium;">The causes of infertility are many and can be complex. Infertility or failure to conceive and give birth to a young one can be due to malnutrition, infections, congenital defects, management errors and ovulatory or hormonal imbalances in the female.</p>
<strong style="font-size:medium;">Sexual cycle</strong>
<p style="font-size:medium;">Cattle have the sexual cycle (oestrus) once in 18-21 days for 18-24 hours. The farmers should closely monitor the animals 4-5 times from early morning to late night. Poor heat deduction can cause increased levels of infertility. Considerable skill is needed to deduct the animals in heat for visible signs. Farmers who maintain good records and spend more time watching the animals obtain better results.</p>
<p style="font-size:medium;"><strong>Tips to avoid infertility</strong></p>
<ul style="font-size:medium;">
<li style="color: #565656;">Breeding should be done during the oestrus period.</li>
<li style="color: #565656;">Animals that do not show oestrus or do not come to cycle should be checked and treated.</li>
<li style="color: #565656;">Deworming once in 6 months should be done for worm infestations to maintain the health status of the animals. A small investment in periodic deworming can bring greater gains in dairying.</li>
<li style="color: #565656;">Cattle should be fed with a well balanced diet with energy, protein, minerals and vitamin supplements. This helps in increased conception rate, healthy pregnancy, safe parturition, low incidence of infections and a healthy calf.</li>
<li style="color: #565656;">Care of young female calves with good nutrition helps them to attain puberty in time with an optimum body weight of 230-250 kgs, suitable for breeding and thereby better conception.</li>
<li style="color: #565656;">Feeding adequate quantity of green fodder during pregnancy will avoid blindness in newborn calves and retention of placenta (after birth).</li>
<li style="color: #565656;">In natural service, breeding history of the bull is very important to avoid congenital defects and infections.</li>
<li style="color: #565656;">Infections of the uterus can be largely avoided by having cows served and calved under hygienic conditions.</li>
<li style="color: #565656;">After 60-90 days of insemination, the animals should be checked for confirmed pregnancy by qualified veterinarians.</li>
<li style="color: #565656;">When conception occurs, the female enters a period of anestrus (not exhibiting regular oestrus cycles) during pregnancy. The gestation (pregnancy) period for cow is about 285 days and for buffaloes, 300 days.</li>
<li style="color: #565656;">Unwarranted stress and transportation should be avoided during the last stages of pregnancy.</li>
<li style="color: #565656;">The pregnant animal should be housed away from the general herd for better feeding management and parturition care.</li>
<li style="color: #565656;">Pregnant animals should be drained of their milk two months before delivery and given adequate nutrition and exercise. This helps in improving the health of the mother, delivery of a healthy calf with average birth weight, low incidence of diseases and early return of sexual cycle.</li>
<li style="color: #565656;">Breeding can be started within four months or 120 days after delivery to achieve the goal of one calf per year for economic and profitable dairy farming, according to them.</li>
</ul>