<h3 >Importance of Pregnancy Diagnosis in Cattle</h3>
<ol style="font-size:medium;">
<li style="color: #565656;">Pregnancy diagnosis should be done after 45-60 days of breeding by qualified veterinary doctor.</li>
<li style="color: #565656;">This<strong> </strong>facilitates optimal feeding and care of pregnant animals in positive animals.</li>
<li style="color: #565656;">This provides clear way to breed the animal in next oestrus in negative animals.</li>
</ol>
<h3>Knowing the Reproductive Status of Animals</h3>
<ul style="font-size:medium;">
<li style="color: #565656;">18 hours (an average of 12-30 hours) of oestrus signs—Normal.</li>
<li style="color: #565656;">Less than 12 hours / absence of oestrus signs-abnormal (anoestrus). </li>
</ul>
<strong style="font-size:medium;">Reasons</strong>
<ol style="font-size:medium;">
<li style="color: #565656;">Failure to detect oestrus signs.</li>
<li style="color: #565656;">Suboestrus, weak or silent oestrus.</li>
<li style="color: #565656;">A low plane of nutrition- lack of energy and protein, deficiency of minerals namely P, Co, Fe, Cu, I, Mn and Vitamin A</li>
<li style="color: #565656;">Failure to recognize that an animal is pregnant.</li>
<li style="color: #565656;">Anoestrus due to uterine pathology such as pyometra, mummified foetus, foetal maceration, mucometra and hydrometra and</li>
<li style="color: #565656;">Insufficient hormonal stimuli.</li>
</ol>
<p style="font-size:medium;"><strong>Management</strong></p>
<ol style="font-size:medium;">
<li style="color: #565656;">Unobserved oestrum may be due to managerial deficiencies and short period of oestrus.</li>
<li style="color: #565656;">The dairy animals should be observed for heat signs at least three times a day.</li>
<li style="color: #565656;">Wall charts, breeding wheels, herd monitors and individual cow records may be used for identify the oestrus.</li>
<li style="color: #565656;">Teaser bulls (vasectomized or by applying apron) are useful in identifying heat in large number of animals especially buffalo cows.</li>
<li style="color: #565656;">Provision of adequate lighting to improve oestrus detection.</li>
<li style="color: #565656;">Silent / weak / Suboestrus are most common in buffalo cows and common in post partum period. In this cyclical changes in the genital organs occurs but the signs of heat are not exhibited or not observed. This requires rectal examination by qualified veterinary doctor.</li>
<li style="color: #565656;">Extra feeding of a concentrate mixture or grains like maize, Cholam, kambu. Etc., and at least small amount of green fodder along with other roughages.</li>
<li style="color: #565656;">Mineral mixture should be properly supplemented.</li>
<li style="color: #565656;">After breeding the animals should be checked for pregnancy within 45-60 days by qualified veterinary doctor.</li>
<li style="color: #565656;">Uterine pathology and hormonal stimuli should be handled by qualified veterinary doctor.</li>
</ol>
