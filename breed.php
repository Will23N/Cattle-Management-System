 <h3>Signs of Oestrus in Cattle</h3>
 <p style="font-size:medium;">Below are some of the signs of oestrus in cattle;</p>
 <ol style="font-size:medium;">
<li style="color: #565656;">Standing to be mounted by other cows.</li>
<li style="color: #565656;">Attempt to mount other cows.</li>
<li style="color: #565656;">Stringy mucous hanging from vulva.</li>
<li style="color: #565656;">Mucus smeared on buttocks.</li>
<li style="color: #565656;">Increased restlessness.</li>
<li style="color: #565656;">Drop in milk yield.</li>
<li style="color: #565656;">Reduced feed intake.</li>
<li style="color: #565656;">Frequent Bellowing.</li>
<li style="color: #565656;">Chin resting on cow’s rump by other cows, tail raising.</li>
<li style="color: #565656;">Vulval oedema.</li>
<li style="color: #565656;">Frequent urination.</li>
</ol>
